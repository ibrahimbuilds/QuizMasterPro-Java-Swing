    private void btn_SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchActionPerformed
    String searchKey = txt_Search.getText().trim();
    
    // If the search bar is empty, just reload all records normally
    if (searchKey.isEmpty()) {
        loadDatabaseResults(); // Call your existing method that reads SELECT *
        return;
    }

    javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); 

    // Search query looking for matches in either roll_no OR student_name
    String query = "SELECT * FROM quiz_results WHERE roll_no LIKE ? OR student_name LIKE ?";
    com.quiz.database.DataBaseHandler.connect();

    try (java.sql.PreparedStatement pstmt = com.quiz.database.DataBaseHandler.conn.prepareStatement(query)) {
        pstmt.setString(1, "%" + searchKey + "%");
        pstmt.setString(2, "%" + searchKey + "%");
        
        try (java.sql.ResultSet rs = pstmt.executeQuery()) {
            while (rs.next()) {
                String roll = rs.getString("roll_no");
                String name = rs.getString("student_name");
                int finalScore = rs.getInt("score");
                model.addRow(new Object[]{roll, name, finalScore});
            }
        }
    } catch (java.sql.SQLException e) {
        System.out.println("Search Error: " + e.getMessage());
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btn_SearchActionPerformed

    private void btn_DeleteResultActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DeleteResultActionPerformed
    int selectedRow = jTable1.getSelectedRow();
    
    if (selectedRow == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please select a student record from the table first!");
        return;
    }

    // Get the Roll Number from the first column (Index 0) of the highlighted row
    String rollNoToDelete = jTable1.getValueAt(selectedRow, 0).toString();

    int confirm = javax.swing.JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to permanently delete records for Roll No: " + rollNoToDelete + "?", 
            "Confirm Deletion", javax.swing.JOptionPane.YES_NO_OPTION);

    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        com.quiz.database.DataBaseHandler.connect();
        String query = "DELETE FROM quiz_results WHERE roll_no = ?";

        try (java.sql.PreparedStatement pstmt = com.quiz.database.DataBaseHandler.conn.prepareStatement(query)) {
            pstmt.setString(1, rollNoToDelete);
            pstmt.executeUpdate();
            
            javax.swing.JOptionPane.showMessageDialog(this, "Record successfully purged.");
            loadDatabaseResults(); // Refresh the table automatically!
            
        } catch (java.sql.SQLException e) {
            System.out.println("Deletion Error: " + e.getMessage());
        }
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btn_DeleteResultActionPerformed

