    private void loadDatabaseResults() {
        // Gain a direct control reference to the empty Table visual model
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Wipe out any design placeholders
        
        String query = "SELECT * FROM quiz_results";
        com.quiz.database.DataBaseHandler.connect();
        
        try (java.sql.Statement stmt = com.quiz.database.DataBaseHandler.conn.createStatement();
             java.sql.ResultSet rs = stmt.executeQuery(query)) {
            
            while (rs.next()) {
                // Read row structural content directly from columns
                String roll = rs.getString("roll_no");
                String name = rs.getString("student_name");
                int finalScore = rs.getInt("score");
                
                // Construct a raw data line array and load it directly into your table view
                model.addRow(new Object[]{roll, name, finalScore});
            }
        } catch (java.sql.SQLException e) {
            System.out.println("Error rendering JTable records: " + e.getMessage());
        }
    }

  

